# Conversão de temperatura com comentários

# Os comentários estão novamente misturados - organize-os corretamente.

# Solicitamos ao usuário a temperatura em graus Celsius
celsius = float(input("Digite a temperatura em graus Celsius: "))

# Fórmula de conversão: F = (C * 9/5) + 32
# Convertendo a temperatura para graus Fahrenhei
fahrenheit = (celsius * 9/5) + 32


# Exibindo o resultado
print("Temperatura em graus Fahrenheit:", fahrenheit)