# Criamos uma lista de números de 1 a 10
numbers = list(range(1, 11))

# Calculamos a soma dos números na lista
total_sum = sum(numbers)

# Calculamos o valor médio dos números na lista
average = total_sum / len(numbers)

# Exibimos os resultados na tela
print("Soma dos números:", total_sum)
print("Valor médio:", average)