# Criação da lista
my_list = []

# Adicionando elementos
my_list.append(10)
my_list.append(20)
my_list.append(30)
print("Depois de adicionar elementos:", my_list)

# Acesso por índice
second_element = my_list[1]
print("Elemento com índice 1:", second_element)

# Modificação de elemento
my_list[1] = 25
print("Depois de modificar o elemento com índice 1:", my_list)

# Removendo elemento
del my_list[2]
print("Depois de remover o elemento com índice 2:", my_list)