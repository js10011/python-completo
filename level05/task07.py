# Matemático Arredondado

# Escreva um programa que solicita ao usuário um número real e arredonda-o para baixo (usando math.floor()),
# para cima (usando math.ceil()) e para o inteiro mais próximo (usando round()).
# Exiba os resultados dos três tipos de arredondamento.

# Escreva seu código aqui
import math
number = float(input("Digite um numero : "))
print(round(number))
print(math.floor(number) )
print(math.ceil(number))
